#define _CRT_SECURE_NO_WARNINGS 1
#include <stdio.h>
#include <string.h>
#include <time.h>
#include <stdlib.h>

void game() 
{
	int ret = 0;
	int guess = 0;
	ret = rand() % 100 + 1;//调用rand函数生成一个随机数
	while (1) 
	{
		printf("请猜数字\n");
		scanf("%d",&guess);
		if (guess > ret)
			printf("猜大了\n");
		else if (guess < ret)
			printf("猜小了\n");
		else 
		{
			system("cls");
			printf("恭喜你猜对了\n");
			break;
		}
	}
}


void menu()
{
	printf("************************************\n");
	printf("****  1.开始游戏    0.退出游戏  ****\n");
	printf("************************************\n");
}

int main() 
{
	int input = 0;
	srand((unsigned int)time(NULL));
	do
	{
		menu();
		scanf("%d", &input);
		switch (input)
		{
		case 1:
			printf("开始游戏\n");
			game();
			break;
		case 0:
			printf("退出游戏\n");
			break;
		default:
			printf("选择错误\n");
		}
	} while (input);
	return 0;
}